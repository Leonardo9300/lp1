package unidade3;

public class SaidaPadrao {

	public static void main(String[] args) {
		// Imprime na saída padrão e salta o cursor para a próxima linha
		System.out.println("Bem vindo ao Java");
		
		// Imprime sem saltar a linha
		System.out.print("Bem vindo ");
		System.out.println("ao Java");
		
		// Imprime concatenando spring e número
		int numero = 10;
		System.out.println("O número é " + numero);

	}

}
